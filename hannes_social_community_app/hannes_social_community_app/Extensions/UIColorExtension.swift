import UIKit

extension UIColor{
 
    static func appThemeColor() -> UIColor{
        return UIColor(red: 61/255, green: 91/255, blue: 151/255, alpha: 1)
    }
    
    static func seporatorColor() -> UIColor{
        return UIColor(red: 220/255, green: 220/255, blue: 220/255, alpha: 1)
    }
}
